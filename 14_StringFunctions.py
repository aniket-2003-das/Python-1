story = "once upon a time there was a youtuber who uploaded python course with notes"

# string functions:-

print(len(story))
print(story.endswith("with notes"))
print(story.count("a"))
print(story.capitalize())
print(story.find("great"))
print(story.replace("youtuber","teacher"))

# len counts no of charectars
# endswith gives a boolean check
# count counts no of charectars
# capitalise capitalises first charectar of the string
# find also gives boolean values
# replace replaces small string segments